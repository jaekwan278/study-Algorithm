package programmers.basicTraining.stringConcatenator;

// 문자열 붙여서 출력하기

import java.util.Scanner;

public class StringConcatenator {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.next();
        String b = sc.next();

        System.out.println(a + b);
    }

}
